    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed
        String atividade,local;
        Date data,hora;
        
        atividade = campoAtividate.getText();
        local = campoLocal.getText();
        data = Data.converteData(campoData.getText());
        
        registrocronograma.setAtividade(atividade);
        registrocronograma.setData(data);
        registrocronograma.setLocal(local);
        
        conogramaService.salvar(registrocronograma);
        dispose();
    }//GEN-LAST:event_salvarActionPerformed

    private void preecherDados(Registrocronograma registroCronograma){
        String dataConograma = Data.formatDate(registroCronograma.getData());
        
        campoAtividate.setText(registroCronograma.getAtividade());
        campoData.setText(Data.retirarSimbolosData(dataConograma));
        campoHora.setText(registroCronograma.getHorario()+"");
        campoLocal.setText(registroCronograma.getLocal());
    }
    
    private void colocarMascaras(){
        DefaultFormatterFactory factory =
                new DefaultFormatterFactory(Campos.createFormatter("##-##-####"));
        campoData.setFormatterFactory(factory);
    }

